
function createBulkPatientSummaryForMario() {
  const ProviderName = 'MARIO MARUTHUR';

  setProperty_(`${prefix_createBulkPatientSumm}${ProviderName}`, getNewIndex_Bulk_());
  SpreadsheetApp.getActiveSpreadsheet().toast(`File will be created for ${ProviderName} soon.`);

  updatePendingStatusBulk_();
}


